package comjavacorejavaWelcome;

public class ShiftOperator {
	
	public static void main (String[] args) {
		
		int a = 10;
		
		int b = 20;
		
		          // Left Shift Operator
		
		System.out.println(a<<3);  // 10*2^3 / 10*8= 80
		
		          // Right Shift Operator
		
		System.out.println(b>>3);  // 10/2^3 / 10/8 = 2
		
		
		
	}

}
