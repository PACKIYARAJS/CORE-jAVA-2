package comjavacorejavaWelcome;

public class AssignmentOperator {
	
	public static void main (String[] args) {
		
		int a = 10;
		
		int b = 20;
		
		int c = 30;
		
		int d = 40;
		
		int e = 10;
		
		int f = 30;
		
		System.out.println(a+=b);    
		
		System.out.println(a-=b);
		
		System.out.println(a*=d);
		
		System.out.println(f/=e);
		
		System.out.println(a%=c);
		
		
	
		
	}

}
