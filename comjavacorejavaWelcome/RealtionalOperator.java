package comjavacorejavaWelcome;

public class RealtionalOperator {
	
	public static void main (String [] args) {
		
		int a = 10;
		
		int b = 20;
		
		int c = 30;
		
		int d = 10;
		 
		System.out.println(a==d);  // Equal to
		
		System.out.println(a!=c);  // Not Equal
		
		System.out.println(a<b);   // Less than
		
		System.out.println(a>c);   // Greater than
		
		System.out.println(a<=c);  // Less than or Equal
		
		System.out.println(a>=c);  // Greater than or Equal
		
		
	}

}
